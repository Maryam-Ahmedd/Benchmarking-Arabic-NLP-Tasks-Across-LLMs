def ask_fanar(prompt, retries=3):
    for attempt in range(1, retries + 1):
        try:
            response = client.chat.completions.create(
                model=MODEL_NAME,
                messages=[
                    {
                        "role": "user",
                        "content": prompt
                    }
                ],
                temperature=0,
                max_tokens=800
            )

            return response.choices[0].message.content

        except Exception as e:
            error_text = str(e)

            if "504" in error_text or "Gateway Timeout" in error_text:
                print(f"Attempt {attempt} failed: Fanar timeout.")
            elif "401" in error_text or "Unauthorized" in error_text:
                return "ERROR: API token is wrong or expired."
            elif "404" in error_text or "not found" in error_text.lower():
                return "ERROR: Model name or endpoint is not available."
            else:
                print(f"Attempt {attempt} failed: {error_text[:300]}")

            if attempt < retries:
                time.sleep(5)

    return "ERROR: Model timeout or provider unavailable after 3 tries."


